package com.gma.exerciser.Pojo;

public class Dictionary {

	private String name;
	private String description;
	private int source_lang;
	private int dest_lang;
	private int level;
	private int id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getSource_lang() {
		return source_lang;
	}
	public void setSource_lang(int source_lang) {
		this.source_lang = source_lang;
	}
	public int getDest_lang() {
		return dest_lang;
	}
	public void setDest_lang(int dest_lang) {
		this.dest_lang = dest_lang;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

}